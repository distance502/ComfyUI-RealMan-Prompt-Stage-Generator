def _iter_signal_terms(value: str) -> list[str]:
    text = str(value or "").strip()
    if not text or text == "——":
        return []
    parts = [text]
    for splitter in ("，", "、", ";", "；"):
        next_parts: list[str] = []
        for part in parts:
            next_parts.extend(part.split(splitter))
        parts = next_parts
    return _dedupe([part.strip() for part in parts if part.strip() and part.strip() != "——"])


